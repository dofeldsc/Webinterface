function licName($lic, $license)
{
    $settings = require_once 'settings.php';
    if (isset($license[$settings['language']][$lic])) {
        return $license[$settings['language']][$lic];
    } elseif (isset($license['en'][$lic])) {
        return $license['en'][$lic];
    } else {
        return $lic;
    }
}
